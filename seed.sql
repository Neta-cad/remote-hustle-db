INSERT INTO submissions (participant_id, task_id, submission_url, notes, status)
SELECT 
    p.participant_id,
    t.task_id,
    'https://github.com/' || LOWER(REPLACE(u.full_name, ' ', '')) || '/remote-hustle-db',
    'My database submission for Stage 1',
    (ARRAY['pending', 'pending', 'pending', 'reviewed', 'accepted'])
    [floor(random() * 5 + 1)::int]
FROM participants p
JOIN users u ON p.user_id = u.user_id
CROSS JOIN tasks t
LIMIT 50;



INSERT INTO evaluations (submission_id, judge_id, score, max_score, feedback, status)
SELECT 
    s.submission_id,
    j.user_id,
    floor(random() * 40 + 60)::int,
    100,
    (ARRAY[
        'Great database design with proper normalization',
        'Good work but missing some constraints',
        'Excellent use of foreign keys and relationships',
        'Well structured with good seed data',
        'Impressive security implementation with roles'
    ])[floor(random() * 5 + 1)::int],
    'completed'
FROM submissions s
CROSS JOIN (
    SELECT user_id FROM users WHERE role = 'judge' LIMIT 1
) j
WHERE s.status = 'reviewed' OR s.status = 'accepted'
LIMIT 20;







INSERT INTO analytics (participant_id, stage_id, total_submissions, total_score, average_score, highest_score, lowest_score, pass_status)
SELECT 
    p.participant_id,
    s.stage_id,
    COUNT(sub.submission_id),
    COALESCE(SUM(e.score), 0),
    COALESCE(AVG(e.score), 0.00),
    COALESCE(MAX(e.score), 0),
    COALESCE(MIN(e.score), 0),
    CASE 
        WHEN AVG(e.score) >= 70 THEN 'passed'
        WHEN AVG(e.score) < 70 AND AVG(e.score) > 0 THEN 'failed'
        ELSE 'pending'
    END
FROM participants p
CROSS JOIN stages s
LEFT JOIN submissions sub ON sub.participant_id = p.participant_id
LEFT JOIN evaluations e ON e.submission_id = sub.submission_id
GROUP BY p.participant_id, s.stage_id;





